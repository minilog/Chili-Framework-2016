void Ball::Update()
{
	x += vx;
	y += vy;

	if (x < 0)
	{
		x = 0;
		vy = 0;
	}
	else if (x + width > Graphics::ScreenWidth)
	{
		x = Graphics::ScreenWidth - width;
		vy = 0;
	}

	if (y < 0)
	{
		y = 0;
		vy = -vy;
	}
	else if (y + height > Graphics::ScreenHeight)
	{
		y = Graphics::ScreenHeight - height;
		vy = -vy;
	}

	++time;
	if (time >= 60 * (10 / speedIncreasePer10Second))
	{
		if (vx >= 0)
		{
			++vx;
		}
		else
		{
			--vx;
		}
		time = 0;
	}

	//if (vx > maxSpeed)
	//	vx = maxSpeed;
	//else if (vx < -maxSpeed)
	//	vx = -maxSpeed;
	//if (vy > maxSpeed)
	//	vy = maxSpeed;
	//else if (vy < -maxSpeed)
	//	vy = -maxSpeed;
}

void Ball::InteractPlayer(const Player & player)
{
	int left_player = player.GetCenterX() - Player::width / 2;
	int top_player = player.GetCenterY() - Player::height / 2;
	int right_player = player.GetCenterX() + Player::width / 2;
	int bottom_player = player.GetCenterY() + Player::height / 2;

	if (GameCollision::RectangleAndCircle(player.GetBound(), GetCenterX(), GetCenterY(), radius))
	{
		if (right_player <= GetCenterX())
		{
			if (vx <= 0)
			{
				vx = -vx;
			}
			else
			{
				x += Player::width / 2 + width / 2 - abs(player.GetCenterX() - GetCenterX());
			}

			if (player.GetCenterY() - Player::height / 4 > GetCenterY())
			{
				vy -= abs(vx) / 6;
			}
			else if (player.GetCenterY() + Player::height / 4 < GetCenterY())
			{
				vy += abs(vx) / 6;
			}
		}
		else if (left_player >= GetCenterX())
		{
			if (vx >= 0)
			{
				vx = -vx;
			}
			else
			{
				x -= Player::width / 2 + width / 2 - abs(player.GetCenterX() - GetCenterX());
			}


			if (player.GetCenterY() - Player::height / 4 > GetCenterY())
			{
				vy -= abs(vx) / 2;
			}
			else if (player.GetCenterY() + Player::height / 4 > GetCenterY())
			{
				vy += abs(vx) / 2;
			}
		}
		else if (top_player >= GetCenterY())
		{
			if (vy >= 0)
			{
				vy = -vy;
			}
			else
			{
				y -= abs(GetCenterY() - player.GetCenterY()) / 10;
			}
		}
		else if (bottom_player <= GetCenterY())
		{
			if (vy <= 0)
			{
				vy = -vy;
			}
			else
			{
				y += abs(GetCenterY() - player.GetCenterY()) / 10;
			}
		}
		else if (player.GetCenterX() <= GetCenterX())
		{
			if (vx <= 0)
			{
				vx = -vx;
			}
			else
			{
				x += Player::width / 2 + width / 2;
			}
		}
		else if (player.GetCenterX() >= GetCenterX())
		{
			if (vx >= 0)
			{
				vx = -vx;
			}
			else
			{
				x -= Player::width / 2 + width / 2;
			}
		}
	}
}
